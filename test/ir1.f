INTEGER FUNCTION B(A,C)
INTEGER A, B, C, D
B = A + C * 2
IF (B .EQ. A) THEN
  D = A / C
ENDIF
RETURN
END

SUBROUTINE DO_LOOP(X)
INTEGER X, I, J
DO I = 1, 10, 2
  J = B(I,X)
ENDDO
RETURN
END

SUBROUTINE C(X)
INTEGER X, C
C = 5
IF (X .GT. C) THEN
  C = X + 1
ELSE
  C = X - 1
ENDIF
RETURN
END

PROGRAM A
REAL X, Y
INTEGER I
CALL C(I)
X = 10.0
Y = (X * X) - 3.0
STOP
END